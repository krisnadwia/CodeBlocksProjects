/* Mengisi variabel i yg bertype in dengan nilai 5 dan menuliskannya */
#include<stdio.h>
int main()
{/* Kamus */
    int i = 5; /* deklarasi dan inisialisasi nilai variabel i dengan 5 */
    int j = 10;
    float keliling = 2 * 3.14 * 5.5; /* keliling dari sebuah lingkaran yang jari-jarinya 5.5. cm */
    /* Algoritma */
    printf("Ini nilai i : %d \n", i); /*untuk mengamati dampak inisialisasi*/
    printf("Ini nilai j : %d \n", j);
    printf("Keliling dari sebuah lingkaran yang jari-jarinya 5.5 cm : %.2f \n", keliling);
    return 0;
}
